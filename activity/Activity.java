System.out.println(j); 


int k = 30;    // int into double
double l = (double) k;
System.out.println(l);
System.out.println(k); 


int m = 20;    // int into char
char n = (char) m;
System.out.println(n);
System.out.println(m); 

 
double value = 56.09; // double to int
int x = (int) value;

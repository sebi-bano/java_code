System.out.println(c); 


int a = 10;    // int into byte
byte d = (byte) a;
System.out.println(d);
System.out.println(a); 

int f = 90;    // int into short
short e = (short) f;
System.out.println(e);
System.out.println(f); 


int h = 500;    // int into long
long g = (long) h;
System.out.println(g);
System.out.println(h); 


int j = 16;    // int into float
float i = (float) j;
